#!/usr/bin/env node
// Post-deploy: read deployments/arc-testnet.json and propagate addresses to:
//   - README.md   (replace the address table)
//   - .env        (append FORUM_*_ADDRESS lines if missing)
//   - sdk-ts/src/deployments.ts  (new file)
//   - sdk-py/src/forum_arc/deployments.py  (new file)
//
// Idempotent: re-running with the same deployments.json is a no-op.

import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { join, dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, '..', '..');
process.chdir(REPO_ROOT);

const DEPLOY_PATH = 'deployments/arc-testnet.json';
if (!existsSync(DEPLOY_PATH)) {
  console.error(`ERROR: ${DEPLOY_PATH} not found. Run scripts/deploy.mjs first.`);
  process.exit(1);
}
const d = JSON.parse(readFileSync(DEPLOY_PATH, 'utf8'));
const c = d.contracts;
const reg = c.BuilderCodeRegistry.address;
const cfg = c.KeeperConfig.address;
const trk = c.TrackRecord.address;
const fee = c.FeeDistributor.address;

console.log(`Updating with deployments from chain ${d.chainId}:`);
console.log(`  BuilderCodeRegistry  ${reg}`);
console.log(`  KeeperConfig         ${cfg}`);
console.log(`  TrackRecord          ${trk}`);
console.log(`  FeeDistributor       ${fee}`);

// 1) README.md — replace the address table
const readmePath = 'README.md';
let readme = readFileSync(readmePath, 'utf8');
const tableRe = /\| `BuilderCodeRegistry` \|[^\n]*\n\| `KeeperConfig` \|[^\n]*\n\| `TrackRecord` \|[^\n]*\n\| `FeeDistributor` \|[^\n]*\n/;
const newTable =
  `| \`BuilderCodeRegistry\` | [\`${reg}\`](https://testnet.arcscan.app/address/${reg}) |\n` +
  `| \`KeeperConfig\` | [\`${cfg}\`](https://testnet.arcscan.app/address/${cfg}) |\n` +
  `| \`TrackRecord\` | [\`${trk}\`](https://testnet.arcscan.app/address/${trk}) |\n` +
  `| \`FeeDistributor\` | [\`${fee}\`](https://testnet.arcscan.app/address/${fee}) |\n`;
if (tableRe.test(readme)) {
  readme = readme.replace(tableRe, newTable);
  writeFileSync(readmePath, readme);
  console.log('README.md  updated');
} else {
  console.warn('README.md  table pattern not found — manual edit needed');
}

// 2) .env — append FORUM_*_ADDRESS lines if missing
const envPath = '.env';
let env = existsSync(envPath) ? readFileSync(envPath, 'utf8') : '';
const lines = [
  ['FORUM_REGISTRY_ADDRESS', reg],
  ['FORUM_CONFIG_ADDRESS', cfg],
  ['FORUM_TRACKRECORD_ADDRESS', trk],
  ['FORUM_FEEDISTRIBUTOR_ADDRESS', fee],
];
for (const [key, val] of lines) {
  const re = new RegExp(`^${key}=.*$`, 'm');
  if (re.test(env)) {
    env = env.replace(re, `${key}=${val}`);
  } else {
    if (env.length > 0 && !env.endsWith('\n')) env += '\n';
    env += `${key}=${val}\n`;
  }
}
writeFileSync(envPath, env);
console.log('.env       updated');

// 3) sdk-ts/src/deployments.ts
const tsDepl = `// Auto-generated by scripts/update-after-deploy.mjs — do not edit by hand.
import type { ForumAddresses } from './types.js';

export const ARC_TESTNET_DEPLOYMENT: ForumAddresses = {
  registry: '${reg}',
  config: '${cfg}',
  trackRecord: '${trk}',
  feeDistributor: '${fee}',
};
`;
writeFileSync('sdk-ts/src/deployments.ts', tsDepl);
console.log('sdk-ts/src/deployments.ts  written');

// 4) sdk-py/src/forum_arc/deployments.py
const pyDepl = `# Auto-generated by scripts/update-after-deploy.mjs — do not edit by hand.
from .client import ForumAddresses

ARC_TESTNET_DEPLOYMENT = ForumAddresses(
    registry="${reg}",
    config="${cfg}",
    track_record="${trk}",
    fee_distributor="${fee}",
)
`;
writeFileSync('sdk-py/src/forum_arc/deployments.py', pyDepl);
console.log('sdk-py/src/forum_arc/deployments.py  written');

console.log('\nAll updated. Don\'t forget: `git add -A && git commit`.');
