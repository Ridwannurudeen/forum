#!/usr/bin/env node
// Post-deploy: read deployments/arc-testnet.json and propagate public
// addresses to README.md plus the TypeScript and Python SDK deployment files.
//
// This script intentionally does not edit .env. Secrets and local environment
// files stay outside automated address propagation.

import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, "..", "..");
process.chdir(REPO_ROOT);

const DEPLOY_PATH = "deployments/arc-testnet.json";
if (!existsSync(DEPLOY_PATH)) {
  console.error(
    `ERROR: ${DEPLOY_PATH} not found. Run scripts/deploy.mjs first.`,
  );
  process.exit(1);
}

const d = JSON.parse(readFileSync(DEPLOY_PATH, "utf8"));
const c = d.contracts;
const reg = c.BuilderCodeRegistry.address;
const cfg = c.KeeperConfig.address;
const trk = c.TrackRecord.address;
const fee = c.FeeDistributor.address;
const trv2 = c.TrackRecordV2?.address;
const pool = c.AgentPool?.address;
const bond = (c.SlashBondV1_1 ?? c.SlashBond)?.address;
const kernel = (c.RiskKernelV2 ?? c.RiskKernel)?.address;
const vault = (c.CovenantVaultV1_2 ?? c.CovenantVault)?.address;

console.log(`Updating with deployments from chain ${d.chainId}:`);
for (const [name, addr] of [
  ["BuilderCodeRegistry", reg],
  ["KeeperConfig", cfg],
  ["TrackRecord", trk],
  ["FeeDistributor", fee],
  ["TrackRecordV2", trv2],
  ["AgentPool", pool],
  ["SlashBond", bond],
  ["RiskKernel", kernel],
  ["CovenantVault", vault],
]) {
  if (addr) console.log(`  ${name.padEnd(20)} ${addr}`);
}

const readmePath = "README.md";
let readme = readFileSync(readmePath, "utf8");
const tableRe =
  /\| `BuilderCodeRegistry` \|[^\n]*\n\| `KeeperConfig` \|[^\n]*\n\| `TrackRecord` \|[^\n]*\n\| `FeeDistributor` \|[^\n]*\n/;
const newTable =
  `| \`BuilderCodeRegistry\` | [\`${reg}\`](https://testnet.arcscan.app/address/${reg}) |\n` +
  `| \`KeeperConfig\` | [\`${cfg}\`](https://testnet.arcscan.app/address/${cfg}) |\n` +
  `| \`TrackRecord\` | [\`${trk}\`](https://testnet.arcscan.app/address/${trk}) |\n` +
  `| \`FeeDistributor\` | [\`${fee}\`](https://testnet.arcscan.app/address/${fee}) |\n`;
if (tableRe.test(readme)) {
  readme = readme.replace(tableRe, newTable);
  writeFileSync(readmePath, readme);
  console.log("README.md updated");
} else {
  console.warn("README.md table pattern not found; manual edit may be needed");
}

const optionalTs = [
  ["trackRecordV2", trv2],
  ["agentPool", pool],
  ["slashBond", bond],
  ["riskKernel", kernel],
  ["covenantVault", vault],
]
  .filter(([, addr]) => Boolean(addr))
  .map(([key, addr]) => `  ${key}: '${addr}',\n`)
  .join("");

const tsDepl = `// Auto-generated by scripts/update-after-deploy.mjs - do not edit by hand.
import type { ForumAddresses } from './types.js';

export const ARC_TESTNET_DEPLOYMENT: ForumAddresses = {
  registry: '${reg}',
  config: '${cfg}',
  trackRecord: '${trk}',
  feeDistributor: '${fee}',
${optionalTs}};
`;
writeFileSync("sdk-ts/src/deployments.ts", tsDepl);
console.log("sdk-ts/src/deployments.ts written");

const optionalPy = [
  ["track_record_v2", trv2],
  ["agent_pool", pool],
  ["slash_bond", bond],
  ["risk_kernel", kernel],
  ["covenant_vault", vault],
]
  .filter(([, addr]) => Boolean(addr))
  .map(([key, addr]) => `    ${key}="${addr}",\n`)
  .join("");

const pyDepl = `# Auto-generated by scripts/update-after-deploy.mjs - do not edit by hand.
from .client import ForumAddresses

ARC_TESTNET_DEPLOYMENT = ForumAddresses(
    registry="${reg}",
    config="${cfg}",
    track_record="${trk}",
    fee_distributor="${fee}",
${optionalPy})
`;
writeFileSync("sdk-py/src/forum_arc/deployments.py", pyDepl);
console.log("sdk-py/src/forum_arc/deployments.py written");

console.log("\nAll public address files updated.");
